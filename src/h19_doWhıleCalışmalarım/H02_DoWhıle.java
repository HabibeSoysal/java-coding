package h19_doWhıleCalışmalarım;

public class H02_DoWhıle {
    public static void main(String[] args) {
        /*
        ’m’ harfinden baslayarak ‘c’ harfine kadar tum harfleri yazdiri
         */

        char ilkHarf='m', sonHarf='c';

        char tanı=ilkHarf;
        do {
            System.out.print(tanı + " ");
            tanı--;
        }while (tanı>=sonHarf);








    }
}
