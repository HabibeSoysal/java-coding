package day07_practice;

public class C02_IfStatements {
    public static void main(String[] args) {
        int sayi = 10;
        if (sayi > 0) {
            System.out.println("sayi pozitif");
        }
        if (sayi % 2 == 0) {
            System.out.println("sayi çift");
        }
        if (sayi % 5 == 0) {
            System.out.println("sayi 5 in katı");
        }
    }
}